  dp = [[0, arr[0][0]], [0, arr[1][0]]]
  for j in range(2, N+1):
    dp[0].append(max(dp[1][j-1], dp[1][j-2]) + arr[0][j-1])
    dp[1].append(max(dp[0][j-1], dp[0][j-2]) + arr[1][j-1])
